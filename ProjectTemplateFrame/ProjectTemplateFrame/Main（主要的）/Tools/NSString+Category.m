#ifdef  iOSLess8_0
-(BOOL)containsString:(NSString *)str
{
    if (!str.length) {
        return YES;
    }
    NSRange range = [self rangeOfString:str];
    if(range.location != NSNotFound) return YES;
    return NO;
}
